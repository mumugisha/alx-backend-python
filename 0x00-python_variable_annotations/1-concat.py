#!/usr/bin/env python3
"""Type-annotated function concat that takes two strings
and returns their concatenation.
"""


def concat(str1: str, str2: str) -> str:
    """
    Arguments:
    - str1: The first string
    - str2: The second string

    Returns:
    A concatenated string (str1 + str2)
    """
    return str1 + str2
